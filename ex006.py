#Crie um algoritimo que leia um numero e mostre o seu dobro, triplo e raiz quadrada
num = int(input("Digite um numero: "))
print("O dobro desse numero {} é igual a: {}".format(num, num*2))
print("O triplo do numero {} é igual a: {}".format(num, num*3))
print("a Raiz quadrada do numero {} é igual a: {:.2f}".format(num, num**(1/2)))
